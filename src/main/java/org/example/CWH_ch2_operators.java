package org.example;

public class CWH_ch2_operators {
    public static void main(String[] args){
        //arithmetic operators
        int a=4;
        //Modulo operators
        int b=6*a;
       // c=4.8%1.1;--->returns decimal remainder
        //Assignment Operators
        b+=2;
        System.out.println(a+b);
        System.out.println(6==6);
        System.out.println(64>6);//comparison/relational operator
        System.out.println(64>5 || 64>98); //logical operator
        System.out.println(2&3);//bitwise operators
    }
}
